# range function
# range(start, stop , step size)
# stop will always go from

a=range(2,15,3)
for i in a:
    print(i)

# with step size as negative value
a = range(10,0,-1)
for i in a:
    print(i)

# with everything as negative value
a = range(-1,-10,-1)
for i in a:
    print(i)

