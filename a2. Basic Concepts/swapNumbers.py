## Swap of 2 Numbers
number_1 = input("enter the value of num_1 ")
number_2 = input("enter the value of num_2 ")
print("Before Swapping")
print("the value of num1: " + number_1)
print("the value of num2: " + number_2)

temp = number_1
number_1 = number_2
number_2 = temp

print("After Swapping")
print("the value of num1: " + number_1)
print("the value of num2: " + number_2)
