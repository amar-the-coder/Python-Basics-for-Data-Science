a, b = 5, 4
print(a & b)  # and
print(a | b)  # or
print(a ^ b)  # xor
print(~a)  # complement
print(a << 2)  # left shift - formula X<<n = X*2 raise to power n
print(a >> 2)  # right shift - formula X>>n = X/2 raise to power n

